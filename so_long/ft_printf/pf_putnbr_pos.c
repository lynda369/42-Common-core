#include "ft_printf.h"

void	pf_putnbr_pos(unsigned int nb, int *len)
{
	if (nb >= 10)
	{
		pf_putnbr_pos(nb / 10, len);
	}
	pf_putchar(nb % 10 + '0', len);
}
